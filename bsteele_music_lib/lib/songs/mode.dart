enum Mode {
  ionian, // The standard major scale (e.g., C-D-E-F-G-A-B-C).
  dorian, // A minor mode with a raised 6th (e.g., D-E-F-G-A-B-C-D).
  phrygian, // A minor mode with a lowered 2nd (e.g., E-F-G-A-B-C-D-E).
  lydian, // A major mode with a raised 4th (e.g., F-G-A-B-C-D-E-F).
  mixolydian, // A major mode with a lowered 7th (e.g., G-A-B-C-D-E-F-G).
  aeolian, // The natural minor scale (e.g., A-B-C-D-E-F-G-A).
  locrian, // A diminished mode, rarely used (e.g., B-C-D-E-F-G-A-B).
}
